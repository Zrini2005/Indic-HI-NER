    # Compiled regex patterns
    # ══════════════════════════════════════════════════════════════════════════
    _DEV_TO_ASCII = str.maketrans("०१२३४५६७८९", "0123456789")
    _RE_DIGIT      = re.compile(r"^\d+(\.\d+)?$")
    _RE_HINDI_DIG  = re.compile(r"^[०-९]+([.][०-९]+)?$")
    _RE_YEAR       = re.compile(r"^(1[0-9]{3}|20[0-9]{2})$")
    _RE_HHMM       = re.compile(r"^\d{1,2}:\d{2}(:\d{2})?$")

    def __init__(self) -> None:
        # Build location-suffix regex once
        suf_pat = "|".join(
            re.escape(s) for s in sorted(self.LOCATION_SUFFIX, key=len, reverse=True)
        )
        self._re_loc_suffix = re.compile(f"({suf_pat})$")

        # Build all multi-token keyword lookup sets (longest-first for matching)
        self._all_loc_kw    = {**self.LOCATION_KEYWORD, **self.LOCATION_GEO_FEATURE,
                               **self.LOCATION_RELIGIOUS_SITE, **self.LOCATION_TRANSPORT}
        self._all_org_kw    = {**self.ORG_KEYWORD, **self.ORG_SUFFIX}

    # ── Helpers ────────────────────────────────────────────────────────────────

    def _is_numeric(self, tok: str) -> bool:
        tok = tok.strip().rstrip("।,!?:;").replace(",", "")
        return bool(self._RE_DIGIT.match(tok) or self._RE_HINDI_DIG.match(tok))

    def _to_ascii_digits(self, tok: str) -> str:
        return tok.translate(self._DEV_TO_ASCII)

    def _is_year_token(self, tok: str) -> bool:
        tok = tok.strip().rstrip("।,!?:;").replace(",", "")
        return bool(self._RE_YEAR.match(self._to_ascii_digits(tok)))

    def _find_agent_before_ne(self, tokens: List[str], ne_idx: int) -> int:
        """
        Walk backward from ne_idx-1, skipping genitive particles (के/की/का),
        and return the index of the actual agent token.

        Example: "भारत के विकास ने" → ne_idx=3 → returns 2 (विकास)
                 "रिलायंस ने"       → ne_idx=1 → returns 0 (रिलायंस)
        """
        j = ne_idx - 1
        while j >= 0 and tokens[j].strip() in self.GENITIVE_PARTICLES:
            j -= 1
        return j

    def _walk_back_numeric(self, tokens: List[str], start: int) -> int:
        """Walk backward collecting numeric tokens; returns first index."""
        i = start - 1
        while i >= 0 and (
            self._is_numeric(tokens[i])
            or tokens[i] in ("और", "तथा", "एवं", "से", "व", ",")
        ):
            i -= 1
        return i + 1

    def _mk(self, start: int, end: int, etype: str, conf: float,
            rid: str, rname: str, etype_ev: str, prio: int) -> RuleMatch:
        return RuleMatch(start, end, etype, conf, rid, rname, etype_ev, prio)

    # ── Main entry point ───────────────────────────────────────────────────────

    def apply(self, tokens: List[str]) -> List[RuleMatch]:
        """
        Apply all 560+ rules to a token list.
        Returns flat list of RuleMatch; ConfidenceVoter resolves overlaps.
        """
        matches: List[RuleMatch] = []
        n = len(tokens)
        mk = self._mk

        for i, raw_tok in enumerate(tokens):
            tok = raw_tok.strip().rstrip("।,!?:")

            # ═══════════════════════════════════════════════════════════════
            # PERSON — structural rules (P318–P345 + earlier lexicons)
            # ═══════════════════════════════════════════════════════════════

            # P_S01: Honorific / title prefix → next 1-3 tokens = PERSON
            if tok in self.PERSON_HONORIFIC_PREFIX:
                conf = self.PERSON_HONORIFIC_PREFIX[tok]
                j = i + 1
                while j < n and tokens[j].strip() in self.POSTPOSITIONS:
                    j += 1

                # Guard: honorific can be part of institution names like
                # "डॉ एपीजे अब्दुल कलाम ... केंद्र" or
                # "डॉ भीमराव आंबेडकर ... हवाई अड्डा".
                in_institution_context = False
                for s in range(i + 1, min(i + 10, n)):
                    probe = tokens[s].strip().rstrip("।,!?:;")
                    if probe in self.POSTPOSITIONS:
                        break
                    if (
                        probe in self.ORG_KEYWORD
                        or probe in self.LOCATION_TRANSPORT
                        or probe in self.INSTITUTION_CONTEXT_TOKENS
                    ):
                        in_institution_context = True
                        break
                    if s + 1 < n:
                        probe2 = tokens[s + 1].strip().rstrip("।,!?:;")
                        if f"{probe} {probe2}" in self.ORG_SUFFIX:
                            in_institution_context = True
                            break

                if in_institution_context:
                    continue

                if j < n:
                    span_end = min(j + 3, n)
                    for k in range(j, span_end):
                        if tokens[k].strip() in self.POSTPOSITIONS:
                            span_end = k
                            break
                    if j < span_end:
                        matches.append(mk(i, span_end, "PERSON", conf * 0.92,
                                         "P_S01", "honorific_prefix_person",
                                         "prefix", 2))

            # P_S02: Respectful role suffix → preceding token = PERSON
            if tok in self.PERSON_ROLE_SUFFIX and i > 0:
                conf = self.PERSON_ROLE_SUFFIX[tok]
                prev = tokens[i - 1].strip()
                if prev not in self.POSTPOSITIONS:
                    matches.append(mk(i - 1, i, "PERSON", conf,
                                     "P_S02", "role_suffix_person",
                                     "suffix", 2))

            # P_S03: Occupation trigger → adjacent name = PERSON
            if tok in self.PERSON_OCCUPATION_TRIGGER:
                conf = self.PERSON_OCCUPATION_TRIGGER[tok]
                # next span
                if i + 1 < n and tokens[i + 1].strip() not in self.POSTPOSITIONS:
                    matches.append(mk(i + 1, min(i + 3, n), "PERSON", conf,
                                     "P_S03_FWD", "occupation_fwd_person",
                                     "prefix", 2))
                # preceding span
                if i > 0 and tokens[i - 1].strip() not in self.POSTPOSITIONS:
                    matches.append(mk(max(0, i - 2), i, "PERSON", conf,
                                     "P_S03_BWD", "occupation_bwd_person",
                                     "prefix", 2))

            # P_S04: Caste / community surname → token = PERSON
            if tok in self.PERSON_CASTE_SURNAME:
                conf = self.PERSON_CASTE_SURNAME[tok]
                matches.append(mk(i, i + 1, "PERSON", conf,
                                  "P_S04", "caste_surname_person",
                                  "suffix", 1))
                # include preceding first-name token
                if i > 0 and tokens[i - 1].strip() not in self.POSTPOSITIONS:
                    matches.append(mk(i - 1, i + 1, "PERSON", conf * 0.95,
                                     "P_S04_EXT", "caste_surname_span_person",
                                     "suffix", 2))

            # P_S05: Name particle → token (and surrounding) = PERSON
            if tok in self.PERSON_NAME_PARTICLES:
                conf = self.PERSON_NAME_PARTICLES[tok]
                start = i
                if i > 0:
                    prev = tokens[i - 1].strip().rstrip("।,!?:;")
                    if (
                        prev not in self.POSTPOSITIONS
                        and prev not in self.PERSON_HONORIFIC_PREFIX
                        and prev not in self.PERSON_OCCUPATION_TRIGGER
                        and prev not in ("और", "तथा", "एवं", "या")
                    ):
                        start = i - 1
                end   = min(n, i + 2)
                matches.append(mk(start, end, "PERSON", conf,
                                  "P_S05", "name_particle_person",
                                  "keyword", 1))

            # P_S06: Family role → adjacent token = PERSON
            if tok in self.PERSON_FAMILY_ROLE:
                conf = self.PERSON_FAMILY_ROLE[tok]
                # "X के पिता" → X is PERSON
                if i >= 2 and tokens[i - 1].strip() in ("के", "की", "का"):
                    matches.append(mk(max(0, i - 3), i - 1, "PERSON", conf,
                                     "P_S06_GEN", "family_role_genitive",
                                     "keyword", 2))
                # "पिता X" → X is PERSON
                if i + 1 < n and tokens[i + 1].strip() not in self.POSTPOSITIONS:
                    matches.append(mk(i + 1, min(i + 3, n), "PERSON", conf,
                                     "P_S06_FWD", "family_role_fwd",
                                     "keyword", 1))

            # P_S07: Political / admin role prefix → next 1-2 tokens = PERSON
            # Cap at 2 (not 3) — politician names are usually 1-2 tokens and
            # a 3-token window bleeds into following location/org words.
            # Boundary: stop before any known ORG/LOC keyword to avoid bleeding.
            if tok in self.POLITICAL_ROLE_PREFIX:
                conf = self.POLITICAL_ROLE_PREFIX[tok]
                j = i + 1
                if j < n and tokens[j].strip() not in self.POSTPOSITIONS:
                    span_end = min(j + 2, n)          # max 2 name tokens
                    for k in range(j, span_end):
                        kt = tokens[k].strip()
                        if kt in self.ORG_KEYWORD or kt in self.LOCATION_KEYWORD:
                            span_end = k
                            break
                    if j < span_end:
                        matches.append(mk(j, span_end, "PERSON", conf,
                                         "P_S07", "political_role_person",
                                         "prefix", 1))

            # ── P_S08: Ergative "ने" → SINGLE adaptive-confidence PERSON match ──
            #
            # Design rationale (balance vs. neural model)
            # ─────────────────────────────────────────────
            # "ने" is Hindi's ergative case marker → near-certain animate agent.
            # But organisations also take "ने" ("सरकार ने", "कंपनी ने"), so we
            # must not override a confident neural ORG signal.
            #
            # One match only (prevents stacking domination):
            #   base conf 0.71  → score 1.253  → neural ORG > 0.78 overrides
            #   genitive chain  → conf 0.73    → neural ORG > 0.80 overrides
            #   +ergative verb  → conf 0.75    → neural ORG > 0.83 overrides
            #
            # Guard: if agent token is itself an ORG keyword (e.g. "सरकार",
            # "बोर्ड") we skip the PERSON match entirely — the ORG rules handle it.
            # ─────────────────────────────────────────────────────────────────────
            if tok == "ने" and i > 0:
                # Walk back past genitive particles to find actual agent token
                agent_idx = i - 1
                while agent_idx > 0 and tokens[agent_idx].strip() in self.GENITIVE_PARTICLES:
                    agent_idx -= 1
                agent_tok = tokens[agent_idx].strip()

                if (agent_tok not in self.POSTPOSITIONS
                        and agent_tok not in self.ORG_KEYWORD
                        and agent_tok not in self.ORG_SUFFIX):

                    # Adaptive confidence (single match — no stacking)
                    ne_conf = 0.71                           # base
                    has_genitive = (agent_idx < i - 1)      # "X के Y ने"
                    if has_genitive:
                        ne_conf = 0.73

                    # Ergative verb look-ahead (within 4 tokens)
                    found_erg = False
                    for fwd in range(i + 1, min(i + 5, n)):
                        t1 = tokens[fwd].strip().rstrip("।,!?:")
                        if t1 in self.ERGATIVE_VERBS:
                            found_erg = True
                            break
                    for fwd in range(i + 1, min(i + 6, n - 1)):
                        t1 = tokens[fwd].strip().rstrip("।,!?:")
                        t2 = tokens[fwd + 1].strip().rstrip("।,!?:")
                        bigram = t1 + " " + t2
                        if bigram in self.ERGATIVE_VERBS:
                            found_erg = True
                            break
                    if found_erg:
                        ne_conf = min(ne_conf + 0.02, 0.75)

                    matches.append(mk(agent_idx, agent_idx + 1, "PERSON", ne_conf,
                                     "P_S08", "ergative_ne_person",
                                     "keyword", 1))

                    # P_S08_AMB: Ambiguous name directly before "ने" (no genitive chain)
                    # Gets a context-adaptive boost for words like विकास/प्रकाश.
                    # When a known ergative verb frame is present (e.g. "आकलन किया"),
                    # use override strength so very high neural-O does not dominate.
                    if (agent_tok in self.AMBIGUOUS_PERSON_NAMES
                            and not has_genitive
                            and agent_idx == i - 1):
                        amb_conf = 0.86
                        amb_prio = 2
                        if found_erg:
                            amb_conf = 0.93
                            amb_prio = 3
                        matches.append(mk(
                            agent_idx, agent_idx + 1, "PERSON", amb_conf,
                            "P_S08_AMB", "ergative_ne_ambiguous_direct_person",
                            "regex", amb_prio,
                        ))

            # P_S09: "द्वारा" → preceding = PERSON (or ORG, lower weight)
            if tok == "द्वारा" and i > 0:
                prev = tokens[i - 1].strip()
                if prev not in self.POSTPOSITIONS:
                    matches.append(mk(i - 1, i, "PERSON", 0.62,
                                     "P_S09", "dwara_person",
                                     "postposition", 1))

            # P_S10: "का नाम … है" → next tokens = PERSON
            if tok in ("नाम", "नाम:") and i > 0 and i + 1 < n:
                if tokens[i - 1].strip() in ("का", "के", "की"):
                    matches.append(mk(i + 1, min(i + 3, n), "PERSON", 0.76,
                                     "P_S10", "ka_naam_person",
                                     "keyword", 2))

            # P_S11: "इनका / उनका / जिनका नाम" → next = PERSON
            if tok in ("इनका", "उनका", "जिनका") and i + 1 < n:
                if tokens[i + 1].strip() in ("नाम", "नाम:"):
                    if i + 2 < n:
                        matches.append(mk(i + 2, min(i + 4, n), "PERSON", 0.75,
                                         "P_S11", "inka_naam_person",
                                         "keyword", 2))

            # P_S12: "बेटे / बेटी / पत्नी / पति का नाम …" → next = PERSON
            if tok in ("पुत्र", "पुत्री", "बेटे", "बेटी", "पत्नी", "पति") \
                    and i + 2 < n \
                    and tokens[i + 1].strip() in ("का", "के", "की") \
                    and tokens[i + 2].strip() in ("नाम", "नाम:"):
                matches.append(mk(i + 3, min(i + 5, n), "PERSON", 0.76,
                                  "P_S12", "relation_naam_person",
                                  "keyword", 2))

            # P_S13: "जिसने / जिन्होंने" → subject in preceding context = PERSON
            if tok in ("जिसने", "जिन्होंने") and i > 0:
                matches.append(mk(max(0, i - 2), i, "PERSON", 0.65,
                                  "P_S13", "relative_clause_person",
                                  "postposition", 1))

            # P_S14: "आरोपी / पीड़ित / गवाह" → next 1-2 tokens = PERSON
