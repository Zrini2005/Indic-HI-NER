# ── Data structures ────────────────────────────────────────────────────────────

@dataclass
class RuleMatch:
    start_token:  int
    end_token:    int           # exclusive
    entity_type:  str           # PERSON | LOCATION | ORGANIZATION | TIME
    confidence:   float
    rule_id:      str
    rule_name:    str
    evidence_type: str          # regex|keyword|prefix|suffix|postposition|gazetteer
    priority:     int = 1       # 1=hint · 2=strong · 3=override


@dataclass
class VotingDebug:
    token:            str
    final_label:      str
    final_conf:       float
    scores:           Dict[str, float]
    evidence_sources: List[str]


# ── Confidence voter ───────────────────────────────────────────────────────────

class ConfidenceVoter:
    """Log-odds weighted combination of neural + rule evidence."""

    SOURCE_MULTIPLIERS: Dict[str, float] = {
        "neural": 1.00, "regex": 1.60, "keyword": 1.40,
        "gazetteer": 1.50, "prefix": 1.30, "suffix": 1.20,
        "postposition": 0.70,
    }
    PRIORITY_MULTIPLIERS: Dict[int, float] = {1: 1.00, 2: 1.30, 3: 1.70}
    NEURAL_SOURCE_WEIGHT = 1.00

    @staticmethod
    def log_odds(p: float, eps: float = 1e-6) -> float:
        p = max(eps, min(1.0 - eps, p))
        return math.log(p / (1.0 - p))

    @staticmethod
    def sigmoid(x: float) -> float:
        return 1.0 / (1.0 + math.exp(-max(-30.0, min(30.0, x))))

    def vote(
        self,
        tokens:       List[str],
        neural_tags:  List[str],
        neural_confs: List[float],
        rule_matches: List[RuleMatch],
        debug:        bool = False,
    ) -> Tuple[List[str], List[float], Optional[List[VotingDebug]]]:
        n = len(tokens)
        token_rules: List[List[RuleMatch]] = [[] for _ in range(n)]
        for m in rule_matches:
            for i in range(m.start_token, min(m.end_token, n)):
                token_rules[i].append(m)

        final_tags:  List[str]   = []
        final_confs: List[float] = []
        debug_info: Optional[List[VotingDebug]] = [] if debug else None

        for i in range(n):
            scores:  Dict[str, float] = defaultdict(float)
            sources: List[str]        = []

            n_tag  = neural_tags[i]  if i < len(neural_tags)  else "O"
            n_conf = neural_confs[i] if i < len(neural_confs) else 0.5
            scores[n_tag] += self.log_odds(n_conf) * self.NEURAL_SOURCE_WEIGHT
            sources.append(f"neural({n_tag},{n_conf:.2f})")

            for m in token_rules[i]:
                prefix = "B" if i == m.start_token else "I"
                label  = f"{prefix}-{m.entity_type}"
                rule_w = (
                    self.log_odds(m.confidence)
                    * self.SOURCE_MULTIPLIERS.get(m.evidence_type, 1.0)
                    * self.PRIORITY_MULTIPLIERS.get(m.priority, 1.0)
                )
                scores[label] += rule_w
                sources.append(f"{m.rule_id}({label},{m.confidence:.2f})")

            best_label = max(scores, key=lambda k: scores[k])
            n_ev       = 1 + len(token_rules[i])
            final_conf = round(self.sigmoid(scores[best_label] / math.sqrt(n_ev)), 4)
            final_tags.append(best_label)
            final_confs.append(final_conf)

            if debug and debug_info is not None:
                debug_info.append(VotingDebug(
                    token=tokens[i], final_label=best_label, final_conf=final_conf,
                    scores=dict(scores), evidence_sources=sources,
                ))

        # BIO consistency repair
        for i in range(1, n):
            tag = final_tags[i]
            if tag.startswith("I-"):
                etype = tag[2:]
                if final_tags[i - 1] not in (f"B-{etype}", f"I-{etype}"):
                    final_tags[i] = f"B-{etype}"

        return final_tags, final_confs, debug_info


